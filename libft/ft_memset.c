#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = (unsigned char *)b;
	c = (unsigned char)c;
	while (i < len)
	{
		p[i] = c;
		i++;
	}
	return (b);
}
